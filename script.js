function initPageAnimations() {
  const loader = document.getElementById('pageLoader');
  const loaderLine = loader ? loader.querySelector('.loader-line') : null;
  const skipLoader = Boolean(loader && document.documentElement.classList.contains('skip-loader'));
  const skipInitialAnimations = shouldSkipInitialPageAnimations();
  const cleanupLoaderShader = loader && !skipLoader ? initLoaderShaderBackground() : null;
  const minLoaderDuration = loader && !skipLoader ? 1020 : 0;
  const loaderStartedAt = performance.now();
  let loaderProgress = 5;
  let loaderProgressTimer = null;
  let pageRevealed = false;
  let revealFallbackTimer = null;

  const setLoaderProgress = (value) => {
    if (!loaderLine) return;
    loaderProgress = Math.max(0, Math.min(100, value));
    loaderLine.style.setProperty('--loader-progress', `${loaderProgress}%`);
  };

  const stopLoaderProgress = () => {
    if (!loaderProgressTimer) return;
    window.clearInterval(loaderProgressTimer);
    loaderProgressTimer = null;
  };

  const revealPage = () => {
    if (pageRevealed) return;
    pageRevealed = true;

    if (revealFallbackTimer) {
      window.clearTimeout(revealFallbackTimer);
      revealFallbackTimer = null;
    }

    stopLoaderProgress();
    if (loader) loader.classList.add('is-hidden');
    if (typeof cleanupLoaderShader === 'function') {
      window.setTimeout(cleanupLoaderShader, 720);
    }
    document.documentElement.classList.remove('skip-loader');
    document.body.classList.remove('preloading');
    document.body.classList.add('site-ready');

    const reveals = document.querySelectorAll('.reveal');
    if (skipInitialAnimations) {
      reveals.forEach((el) => {
        el.style.transitionDelay = '0ms';
        el.classList.add('reveal-in');
      });
      return;
    }

    if (reveals.length) {
      const observer = new IntersectionObserver((entries) => {
        entries.forEach((entry) => {
          if (entry.isIntersecting) {
            entry.target.classList.add('reveal-in');
            observer.unobserve(entry.target);
          }
        });
      }, { threshold: 0.12 });

      reveals.forEach((el, index) => {
        el.style.transitionDelay = `${Math.min(index * 80, 320)}ms`;

        const rect = el.getBoundingClientRect();
        const isAlreadyVisible = rect.top <= window.innerHeight * 0.94 && rect.bottom >= 0;

        if (isAlreadyVisible) {
          el.classList.add('reveal-in');
          return;
        }

        observer.observe(el);
      });
    }
  };

  const scheduleReveal = () => {
    if (pageRevealed) return;

    stopLoaderProgress();
    setLoaderProgress(100);

    const elapsed = performance.now() - loaderStartedAt;
    const finishDelay = Math.max(220, minLoaderDuration - elapsed);

    window.setTimeout(revealPage, finishDelay);
  };

  if (loader && !skipLoader) {
    setLoaderProgress(loaderProgress);
    loaderProgressTimer = window.setInterval(() => {
      const increment = loaderProgress < 36 ? 5.2 : loaderProgress < 70 ? 3 : loaderProgress < 88 ? 1.1 : .4;
      setLoaderProgress(Math.min(loaderProgress + increment, 92));
    }, 100);

    if (document.readyState === 'complete') {
      scheduleReveal();
    } else {
      window.addEventListener('load', scheduleReveal, { once: true });
    }

    revealFallbackTimer = window.setTimeout(scheduleReveal, 4200);
    return;
  }

  if (skipLoader || skipInitialAnimations) {
    setLoaderProgress(100);
  }

  requestAnimationFrame(() => {
    requestAnimationFrame(revealPage);
  });
}

