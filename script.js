const homeAnchorIds = ['services', 'works', 'merch', 'contact'];
let sectionJumpTimer = 0;

function clearSectionJumpState() {
  document.body.classList.remove('anchor-scrolling', 'section-jump-blur');
  delete document.body.dataset.anchorTarget;
  if (sectionJumpTimer) {
    window.clearTimeout(sectionJumpTimer);
    sectionJumpTimer = 0;
  }
}

function getAnchorScrollTop(target) {
  if (!target) return 0;

  const topbar = document.querySelector('.topbar');
  const topOffset = (topbar?.offsetHeight || 0) + 12;
  const nextTop = window.scrollY + target.getBoundingClientRect().top - topOffset;
  return Math.max(0, nextTop);
}

function startSectionJump(hash, target) {
  const currentY = window.scrollY;
  const targetY = target ? getAnchorScrollTop(target) : 0;
  const distance = Math.abs(targetY - currentY);
  const duration = Math.min(950, Math.max(420, Math.round(distance * 0.55)));

  document.body.classList.add('anchor-scrolling');
  document.body.dataset.anchorTarget = hash;

  if (hash === '#top') {
    history.replaceState(null, '', '#top');
    window.scrollTo({ top: 0, behavior: 'smooth' });
  } else if (target) {
    history.replaceState(null, '', hash);
    window.scrollTo({ top: targetY, behavior: 'smooth' });
  }

  if (sectionJumpTimer) {
    window.clearTimeout(sectionJumpTimer);
  }

  sectionJumpTimer = window.setTimeout(() => {
    clearSectionJumpState();
  }, duration);
}

document.querySelectorAll('a[href="#top"]').forEach(link => {
  link.addEventListener('click', (e) => {
    e.preventDefault();
    startSectionJump('#top');
  });
});

document.querySelectorAll('a[href^="#"]').forEach((link) => {
  const hash = link.getAttribute('href');
  if (!hash || hash === '#top') return;

  const targetId = hash.slice(1);
  if (!homeAnchorIds.includes(targetId)) return;

  link.addEventListener('click', (e) => {
    const target = document.getElementById(targetId);
    if (!target) return;

    e.preventDefault();

    if (target.classList.contains('reveal')) {
      target.classList.add('reveal-in');
    }

    if (target.id === 'works') {
      target.dataset.worksHeadingRevealed = 'true';
      target.querySelectorAll('.scroll-letter-target').forEach((heading) => {
        heading.classList.add('is-letter-revealed');
      });
    }

    startSectionJump(hash, target);
  });
});

document.querySelectorAll('a[href]').forEach(link => {
  const initialHref = link.getAttribute('href');
  if (!initialHref || initialHref.startsWith('#') || link.target === '_blank' || link.hasAttribute('download')) return;

  const url = new URL(initialHref, window.location.href);
  if (url.origin !== window.location.origin) return;
  const isHomePageView = document.body.classList.contains('home-page-view');

  link.addEventListener('click', (e) => {
    if (e.defaultPrevented || e.button !== 0 || e.metaKey || e.ctrlKey || e.shiftKey || e.altKey) return;

    const currentHref = link.getAttribute('href');
    if (!currentHref) return;

    const destinationUrl = new URL(currentHref, window.location.href);
    const shouldSkipLoader = link.hasAttribute('data-skip-loader');
    const isHomeNavigation = /\/index\.html$/i.test(destinationUrl.pathname);

    if (shouldSkipLoader) {
      sessionStorage.setItem('alekseymedia_skip_loader', '1');

      if (!isHomePageView && isHomeNavigation) {
        sessionStorage.setItem('alekseymedia_skip_page_animations', '1');
      }
    }

    if (shouldSkipLoader && !isHomePageView) {
      e.preventDefault();
      window.location.href = currentHref;
      return;
    }

    e.preventDefault();
    window.location.href = currentHref;
  });
});

