  const isWorksSnapModalOpen = () => Boolean(document.querySelector('.gallery-modal:not([hidden])'));

  const queueDesktopSnapUnlock = (delay = 180) => {
    window.clearTimeout(desktopSnapUnlockTimer);
    desktopSnapUnlockTimer = window.setTimeout(() => {
      desktopSnapLock = false;
    }, delay);
  };

