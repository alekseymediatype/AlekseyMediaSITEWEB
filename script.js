function normalizeOrderCheckout(checkoutData) {
  const quantity = Math.max(1, Number(checkoutData.quantity || 1));
  const unitAmount = Number(checkoutData.product_unit_amount || checkoutData.product_amount || 0);
  const shippingAmount = Number(checkoutData.shipping_amount || 0);
  const subtotalAmount = unitAmount * quantity;
  const promoCode = String(checkoutData.promo_code || '').trim().toUpperCase();
  const promoDiscountPercent = Math.max(0, Number(checkoutData.promo_discount_percent || 0));
  const promoValidUntil = String(checkoutData.promo_valid_until || '').trim();
  const promoExpiresAt = promoValidUntil ? Date.parse(promoValidUntil) : NaN;
  const promoIsActive = Boolean(
    promoCode
    && promoDiscountPercent
    && (Number.isNaN(promoExpiresAt) || promoExpiresAt > Date.now())
  );
  const discountAmount = promoIsActive
    ? roundOrderAmount(subtotalAmount * (promoDiscountPercent / 100))
    : 0;
  const totalAmount = Math.max(0, roundOrderAmount(subtotalAmount + shippingAmount - discountAmount));

  checkoutData.quantity = quantity;
  checkoutData.product_unit_amount = unitAmount;
  checkoutData.product_unit_price = formatOrderValue(unitAmount);
  checkoutData.product_amount = subtotalAmount;
  checkoutData.product_price = formatOrderValue(unitAmount);
  checkoutData.product_subtotal_amount = subtotalAmount;
  checkoutData.product_subtotal = formatOrderValue(subtotalAmount);
  checkoutData.shipping_amount = shippingAmount;
  checkoutData.shipping_price = formatOrderValue(shippingAmount);
  checkoutData.promo_code = promoIsActive ? promoCode : '';
  checkoutData.promo_discount_percent = promoIsActive ? promoDiscountPercent : 0;
  checkoutData.promo_valid_until = promoIsActive ? promoValidUntil : '';
  checkoutData.promo_discount_amount = discountAmount;
  checkoutData.promo_discount = formatOrderValue(discountAmount);
  checkoutData.order_total_amount = totalAmount;
  checkoutData.order_total = formatOrderValue(totalAmount);
  checkoutData.payment_link = buildOrderPaymentLink(totalAmount);

  return checkoutData;
}

