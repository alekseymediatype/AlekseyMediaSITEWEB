function initWorksScrollSequence() {
  const worksSection = document.getElementById('works');
  if (!worksSection) return;

  const cards = Array.from(worksSection.querySelectorAll('.work'));
  if (!cards.length) return;

  if (window.matchMedia('(prefers-reduced-motion: reduce)').matches) {
    cards.forEach((card) => card.classList.add('is-scroll-visible'));
    return;
  }

  worksSection.classList.add('works-scroll-sequence-ready');

  const revealCard = (card, index) => {
    window.setTimeout(() => {
      card.classList.add('is-scroll-visible');
    }, index * 140);
  };

  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) return;

      const index = cards.indexOf(entry.target);
      revealCard(entry.target, Math.max(index, 0));
      observer.unobserve(entry.target);
    });
  }, {
    threshold: 0.22,
    rootMargin: '0px 0px -12% 0px',
  });

  cards.forEach((card) => observer.observe(card));
}

