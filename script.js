function initHomeMobileMenu() {
  const toggle = document.querySelector('[data-mobile-menu-toggle]');
  const menu = document.querySelector('[data-mobile-menu]');
  if (!toggle || !menu) return;

  const menuLinks = Array.from(menu.querySelectorAll('a[href]'));

  const closeMenu = () => {
    menu.hidden = true;
    document.body.classList.remove('mobile-menu-open');
    toggle.setAttribute('aria-expanded', 'false');
  };

  const openMenu = () => {
    menu.hidden = false;
    document.body.classList.add('mobile-menu-open');
    toggle.setAttribute('aria-expanded', 'true');
  };

  const toggleMenu = () => {
    if (menu.hidden) {
      openMenu();
      return;
    }
    closeMenu();
  };

  toggle.addEventListener('click', (event) => {
    event.preventDefault();
    toggleMenu();
  });

  menuLinks.forEach((link) => {
    link.addEventListener('click', () => {
      closeMenu();
    });
  });

  menu.addEventListener('click', (event) => {
    if (event.target === menu) {
      closeMenu();
    }
  });

  menu.querySelectorAll('.lang-btn').forEach((button) => {
    button.addEventListener('click', () => {
      closeMenu();
    });
  });

  document.addEventListener('keydown', (event) => {
    if (event.key === 'Escape') {
      closeMenu();
    }
  });

  window.addEventListener('resize', () => {
    if (window.innerWidth > 640) {
      closeMenu();
    }
  });

  closeMenu();
}

function initOrderSlideButtons() {
  const buttons = Array.from(document.querySelectorAll('.order-slide-btn'));
  if (!buttons.length) return;

  buttons.forEach((button) => {
    const handle = button.querySelector('[data-slide-handle]');
    if (!handle) return;
    const simpleClick = button.dataset.simpleClick === 'true';

    let dragging = false;
    let offset = 0;
    let startX = 0;
    let maxOffset = 0;

    const setOffset = (value) => {
      offset = value;
      button.style.setProperty('--slide-offset', `${offset}px`);
      button.style.setProperty('--slide-fill-width', `${offset + 48}px`);
    };

    const reset = () => {
      dragging = false;
      button.classList.remove('is-sliding', 'is-complete');
      setOffset(0);
    };

    const stopDragging = () => {
      window.removeEventListener('pointermove', onPointerMove);
      window.removeEventListener('pointerup', onPointerUp);
      window.removeEventListener('pointercancel', onPointerUp);
    };

    const onPointerMove = (event) => {
      if (!dragging) return;
      const nextOffset = Math.max(0, Math.min(maxOffset, event.clientX - startX));
      setOffset(nextOffset);
    };

    const onPointerUp = () => {
      if (!dragging) return;

      const completed = offset >= maxOffset * 0.88;
      stopDragging();

      if (completed) {
        button.classList.add('is-complete');
        setOffset(maxOffset);
        window.setTimeout(() => {
          if (!button.disabled) {
            button.dispatchEvent(new CustomEvent('order-slide-complete', { bubbles: true }));
          }
          reset();
        }, 120);
        return;
      }

      reset();
    };

    handle.addEventListener('click', (event) => {
      event.preventDefault();
      event.stopPropagation();
    });

    button.addEventListener('click', (event) => {
      event.preventDefault();
      event.stopPropagation();

      if (!simpleClick || button.disabled || dragging) return;

      button.classList.add('is-complete');
      maxOffset = Math.max(0, button.clientWidth - handle.clientWidth - 8);
      setOffset(maxOffset);

      window.setTimeout(() => {
        if (!button.disabled) {
          button.dispatchEvent(new CustomEvent('order-slide-complete', { bubbles: true }));
        }
        reset();
      }, 120);
    });

    handle.addEventListener('pointerdown', (event) => {
      if (button.disabled) return;

      dragging = true;
      maxOffset = Math.max(0, button.clientWidth - handle.clientWidth - 8);
      startX = event.clientX - offset;
      button.classList.add('is-sliding');

      if (handle.setPointerCapture) {
        handle.setPointerCapture(event.pointerId);
      }

      window.addEventListener('pointermove', onPointerMove);
      window.addEventListener('pointerup', onPointerUp);
      window.addEventListener('pointercancel', onPointerUp);
      event.preventDefault();
    });

    reset();
  });
}

function initSectionNavigationState() {
  const navLinks = Array.from(document.querySelectorAll('.nav-center .pill[href^="#"]'));
  if (!navLinks.length) return;

  const items = navLinks
    .map((link) => {
      const target = document.querySelector(link.getAttribute('href'));
      return target ? { link, target } : null;
    })
    .filter(Boolean);

  if (!items.length) return;

  const setActiveLink = (activeLink) => {
    items.forEach(({ link }) => {
      const isActive = link === activeLink;
      link.classList.toggle('is-active', isActive);

      if (isActive) {
        link.setAttribute('aria-current', 'page');
      } else {
        link.removeAttribute('aria-current');
      }
    });
  };

  const updateFromScroll = () => {
    const lockedHash = document.body.dataset.anchorTarget || '';
    if (document.body.classList.contains('anchor-scrolling') && lockedHash) {
      const lockedItem = items.find(({ link }) => link.getAttribute('href') === lockedHash);
      if (lockedItem) {
        setActiveLink(lockedItem.link);
        return;
      }
    }

    const topbar = document.querySelector('.topbar');
    const activationLine = (topbar?.offsetHeight || 0) + 44;
    let activeLink = null;

    items.forEach(({ link, target }) => {
      const top = target.getBoundingClientRect().top;
      const bottom = target.getBoundingClientRect().bottom;
      if (top <= activationLine && bottom > activationLine) {
        activeLink = link;
      }
    });

    setActiveLink(activeLink);
  };

  const updateFromHash = () => {
    if (!window.location.hash) {
      updateFromScroll();
      return;
    }

    const match = items.find(({ link }) => link.getAttribute('href') === window.location.hash);
    if (match) {
      setActiveLink(match.link);
      return;
    }

    updateFromScroll();
  };

  let ticking = false;
  const handleScroll = () => {
    if (ticking) return;
    ticking = true;
    requestAnimationFrame(() => {
      updateFromScroll();
      ticking = false;
    });
  };

  window.addEventListener('scroll', handleScroll, { passive: true });
  window.addEventListener('resize', handleScroll);
  updateFromHash();
  window.addEventListener('hashchange', updateFromHash);
}

