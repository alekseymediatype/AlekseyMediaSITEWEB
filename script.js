function initContactForm() {
  const form = document.querySelector('[data-formspree]');
  if (!form) return;

  const status = form.querySelector('[data-form-status]');
  const submitButton = form.querySelector('.submit');

  const setStatus = (key, state = '') => {
    if (!status) return;
    const lang = getCurrentLanguage();
    status.textContent = getTranslation(lang, key, '');
    status.className = `form-status${state ? ` is-${state}` : ''}`;
  };

  form.addEventListener('submit', async (event) => {
    event.preventDefault();

    if (!form.reportValidity()) return;

    if (submitButton) {
      submitButton.disabled = true;
      submitButton.textContent = getTranslation(getCurrentLanguage(), 'contact_sending', 'Sending...');
    }

    setStatus('contact_sending');

    try {
      const response = await fetch(form.action, {
        method: form.method,
        body: new FormData(form),
        headers: {
          Accept: 'application/json',
        },
      });

      if (!response.ok) {
        throw new Error('Form submission failed');
      }

      form.reset();
      setStatus('contact_success', 'success');
    } catch (error) {
      setStatus('contact_error', 'error');
    } finally {
      if (submitButton) {
        submitButton.disabled = false;
        submitButton.textContent = getTranslation(getCurrentLanguage(), 'contact_submit', 'Send message →');
      }
    }
  });
}

