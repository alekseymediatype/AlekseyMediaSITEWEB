async function claimOrderPromo(code) {
  const normalizedCode = String(code || '').trim().toUpperCase();
  if (!normalizedCode) {
    throw new Error('Promo code is empty');
  }

  const localPromo = getLocalOrderPromo(normalizedCode);
  if (localPromo) {
    return localPromo;
  }

  const clientToken = getOrderPromoClientToken();
  const payload = JSON.stringify({ code: normalizedCode, clientToken });
  let lastError = null;

  for (const base of getOrderApiBaseCandidates()) {
    try {
      const claimResponse = await fetch(`${base}/promo-codes/claim`, {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          Accept: 'application/json',
        },
        body: payload,
      });

      if (claimResponse.ok) {
        const data = await parseOrderApiPayload(claimResponse);
        return {
          code: String(data?.code || normalizedCode).toUpperCase(),
          discountPercent: Number(data?.discountPercent || 0),
          validUntil: data?.validUntil || '',
        };
      }

      if (claimResponse.status === 409) {
        const checkResponse = await fetch(`${base}/promo-codes/check`, {
          method: 'POST',
          headers: {
            'Content-Type': 'application/json',
            Accept: 'application/json',
          },
          body: payload,
        });

        if (checkResponse.ok) {
          const data = await parseOrderApiPayload(checkResponse);
          return {
            code: String(data?.code || normalizedCode).toUpperCase(),
            discountPercent: Number(data?.discountPercent || 0),
            validUntil: data?.validUntil || '',
          };
        }

        const checkErrorPayload = await parseOrderApiPayload(checkResponse);
        lastError = new Error(checkErrorPayload?.message || 'Promo code is unavailable');
        continue;
      }

      const errorPayload = await parseOrderApiPayload(claimResponse);
      lastError = new Error(errorPayload?.message || 'Promo code is unavailable');
    } catch (error) {
      lastError = error;
    }
  }

  throw lastError || new Error('Promo code is unavailable');
}

