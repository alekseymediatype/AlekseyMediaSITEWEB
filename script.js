function getCurrentLanguage() {
  return document.documentElement.lang || localStorage.getItem('alekseymedia_lang') || 'en';
}

function getTranslation(lang, key, fallback = '') {
  const dict = translations[lang] || translations.ru;
  return dict[key] || fallback;
}

function shouldSkipInitialPageAnimations() {
  return document.documentElement.classList.contains('skip-page-animations');
}

let pageScrollLockY = 0;
let pageScrollLockActive = false;

function lockPageScroll() {
  if (pageScrollLockActive) {
    document.body.classList.add('gallery-open');
    return;
  }

  pageScrollLockY = window.scrollY || document.documentElement.scrollTop || 0;
  pageScrollLockActive = true;
  document.documentElement.classList.add('page-scroll-locked');
  document.body.classList.add('gallery-open');
  document.body.style.position = 'fixed';
  document.body.style.top = `-${pageScrollLockY}px`;
  document.body.style.left = '0';
  document.body.style.right = '0';
  document.body.style.width = '100%';
}

function unlockPageScroll() {
  document.body.classList.remove('gallery-open');

  if (!pageScrollLockActive) return;

  const restoreY = pageScrollLockY;
  pageScrollLockActive = false;
  pageScrollLockY = 0;
  document.documentElement.classList.remove('page-scroll-locked');
  document.body.style.position = '';
  document.body.style.top = '';
  document.body.style.left = '';
  document.body.style.right = '';
  document.body.style.width = '';
  window.scrollTo(0, restoreY);
}

function initInitialScrollPosition() {
  if ('scrollRestoration' in history) {
    history.scrollRestoration = 'manual';
  }

  const shouldScrollToTop = !window.location.hash || window.location.hash === '#top';
  if (!shouldScrollToTop) return;

  const resetScroll = () => {
    window.scrollTo(0, 0);
  };

  resetScroll();
  window.addEventListener('pageshow', resetScroll);
}

function applyLanguage(lang) {
  const dict = translations[lang] || translations.ru;
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (dict[key]) el.textContent = dict[key];
  });

  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    if (dict[key]) el.innerHTML = dict[key];
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    if (dict[key]) el.placeholder = dict[key];
  });

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  updateLanguageButtonLabels(lang);

  localStorage.setItem('alekseymedia_lang', lang);
  document.dispatchEvent(new CustomEvent('alekseymedia:languagechange', { detail: { lang } }));
}

