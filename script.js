  const summaryTitle = document.getElementById('orderProductTitle');
  const summaryPrice = document.getElementById('orderProductPrice');
  const summaryMeta = Array.from(document.querySelectorAll('[data-order-product-meta]'));
  const typeLabels = Array.from(document.querySelectorAll('[data-order-product-type]'));
  const hiddenProductSize = document.getElementById('orderHiddenProductSize');
  const hiddenProductArticle = document.getElementById('orderHiddenProductArticle');
  const metaText = [selection?.color, selection?.size].filter(Boolean).join(' • ');
  const articleNumber = getMerchArticleNumber(product.id, selection?.color || product.defaultColor || '', selection?.size || product.defaultSize || '');
