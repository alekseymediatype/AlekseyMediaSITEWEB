const merchGithubBaseUrl = 'https://raw.githubusercontent.com/thosewhoknowmango/alekseymedia-photos/main/';

function resolveMerchAssetUrl(src) {
  const value = String(src || '').trim();
  if (!value) return '';

  const normalizedValue = value.replace(/^\/+/, '');
  const target = /^https?:\/\//i.test(normalizedValue)
    ? normalizedValue
    : normalizedValue.startsWith('IMAGES/')
      ? `${merchGithubBaseUrl}${normalizedValue}`
      : normalizedValue;

  try {
    return encodeURI(decodeURI(target));
  } catch (error) {
    return encodeURI(target);
  }
}

function setMerchImageWithLoading(imageEl, src, alt = '') {
  if (!imageEl) return;

  const targetSrc = resolveMerchAssetUrl(src);
  const frame = imageEl.closest('.gallery-media-frame, .cap-photo-link, .product-modal-image-frame, .order-summary-image-frame');
  const loadToken = `${Date.now()}-${Math.random().toString(36).slice(2)}`;

  imageEl.dataset.loadToken = loadToken;
  imageEl.alt = '';

  if (frame) frame.classList.add('is-loading');

  if (!targetSrc) {
    imageEl.removeAttribute('src');
    imageEl.classList.remove('is-loading');
    if (frame) frame.classList.remove('is-loading');
    return;
  }

  const cleanup = () => {
    imageEl.removeEventListener('load', handleLoad);
    imageEl.removeEventListener('error', handleError);
  };

  const handleLoad = () => {
    if (imageEl.dataset.loadToken !== loadToken) {
      cleanup();
      return;
    }

    imageEl.alt = alt;
    imageEl.classList.remove('is-loading');
    if (frame) frame.classList.remove('is-loading');
    cleanup();
  };

  const handleError = () => {
    if (imageEl.dataset.loadToken !== loadToken) {
      cleanup();
      return;
    }

    imageEl.classList.remove('is-loading');
    if (frame) frame.classList.remove('is-loading');
    cleanup();
  };

  imageEl.addEventListener('load', handleLoad);
  imageEl.addEventListener('error', handleError);
  imageEl.alt = alt;
  imageEl.src = targetSrc;

  if (imageEl.complete) {
    if (imageEl.naturalWidth > 0) {
      handleLoad();
    } else {
      handleError();
    }
  }
}

