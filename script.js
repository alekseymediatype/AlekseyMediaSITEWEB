    let closestDistance = Number.POSITIVE_INFINITY;

    cardSnapYs.forEach((snapY, cardIndex) => {
      const distance = Math.abs(currentY - snapY);

      if (distance < closestDistance) {
        closestDistance = distance;
        closestIndex = cardIndex;
      }
    });

    return closestIndex;
